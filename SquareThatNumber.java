class SquareThatNumber {

    public static void main(String[] args){
// prints numbers 1:12 with the square of the number
        for (int i = 0; i < 13; i++) {
            System.out.print(i+ ":");
            int square;
            square = i*i;
            System.out.println(square);

        }
    }
}
